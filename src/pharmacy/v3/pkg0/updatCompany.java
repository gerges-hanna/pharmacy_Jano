        String sq2=txt2.getText();
        String sq3=txt3.getText();
        String sq4=txt4.getText();
        String sq5=txt5.getText();
        
        double sq12=Double.parseDouble(txt5.getText())+Double.parseDouble(paidN.getText());
        double sqRem=Double.parseDouble(sq4)-sq12;
        String sq6=txt6.getText();
        String sq11=txt7.getText();
