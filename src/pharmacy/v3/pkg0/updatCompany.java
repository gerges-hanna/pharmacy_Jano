        String sq7=s.format(d);
        //Time
        Calendar cal=new GregorianCalendar();
        int hour=cal.get(Calendar.HOUR_OF_DAY);
        int minute=cal.get(Calendar.MINUTE);
        int second=cal.get(Calendar.SECOND);
        String sq8=String.valueOf(hour+":"+minute+":"+second);
        double rem=Double.parseDouble(sq4)-Double.parseDouble(sq5);
        String sq10=String.valueOf(rem);
        String sq9=sq5+" in "+sq7+" \n ";
        String payD="";
        if (Double.parseDouble(sq5)> Double.parseDouble(lbl_Rem.getText())) {
            
            JOptionPane.showMessageDialog(this, "it's more than Remainder");
        }
        else{
            if ( Double.parseDouble(sq5)>0) {
             payD=sq11+"\n"+sq5+" in "+sq7+"\n"+"******************";
            }
            else
            {
                payD=sq11;
            }
            try {
                stmt=con.createStatement();
            } catch (SQLException ex) {
                Logger.getLogger(sign_up.class.getName()).log(Level.SEVERE, null, ex);
            }
            String query="UPDATE Company SET  Name='"+sq1+"',Phone='"+sq2+"',Address='"+sq3+"'"
                    + ",Pay='"+sq12+"',Description='"+sq6+"'"
                    + ",Payment_Date='"+payD+"',Remainder='"+sqRem+"' WHERE ID="+id+"";
            try {
                stmt.executeUpdate(query);
            } catch (SQLException ex) {
                Logger.getLogger(sign_up.class.getName()).log(Level.SEVERE, null, ex);
            }

            JOptionPane.showMessageDialog(this, "Update Done.");
        }
